        private const string iconFolderPath = "Packages/com.deadman.modcomponent.sdk/ModComponent/Assets/Icons/Hinterland";
        private const string gearAssetFolderPath = "Assets/_ModComponent/Assets/GearAssets/Hinterland";

        [DidReloadScripts]
        private static void OnScriptsReloaded()
        {
            if (!Directory.Exists(gearAssetFolderPath))
            {
                Directory.CreateDirectory(gearAssetFolderPath);
                GenerateGearAssets();
            }
            else
            {
                var gearItems = GetGearItems();
                foreach (var item in gearItems)
                {
                    string assetPath = gearAssetFolderPath + "/" + item.Key + ".asset";
                    if (!File.Exists(assetPath))
                    {
                        CreateGearAsset(item);
                    }
                }
            }
        }

        private static void CreateGearAsset(KeyValuePair<string, string> item)
        {
            GearAsset asset = ScriptableObject.CreateInstance<GearAsset>();
            asset.Name = item.Key;

            string iconPath = iconFolderPath + "/" + item.Value + ".png";
            Texture2D icon = AssetDatabase.LoadAssetAtPath<Texture2D>(iconPath);
            asset.Icon = icon;

            string assetPath = gearAssetFolderPath + "/" + item.Key + ".asset";
            AssetDatabase.CreateAsset(asset, assetPath);
        }

